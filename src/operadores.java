public class operadores {
    
    public int suma (int op1, int op2){
        int resultado=0;

        resultado = op1 + op2;

        return resultado;
    }
    public int resta (int op1, int op2){
        int resultado=0;

        resultado = op1 - op2;

        return resultado;
    }
    public int multiplicacion (int op1, int op2){
        int resultado=0;

        resultado = op1 * op2;

        return resultado;
    }
    public int division (int op1, int op2){
        int resultado=0;

        resultado = op1 / op2;

        return resultado;
    }
    public int modulo (int op1, int op2){
        int resultado=0;

        resultado = op1 % op2;

        return resultado;
    }
}
